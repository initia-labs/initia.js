/**
 * EVM JSON-RPC Client
 *
 * Provides HTTP-based JSON-RPC methods for EVM chains (minievm).
 * Used for operations not supported by gRPC like transaction receipts and logs.
 */

import { httpRequest, type HttpClientConfig } from './http-client'
import { validateHeaderConflict } from './headers'
import { ContractError } from '../errors'
import type { AuthConfig, HttpRequestOptions } from './types'

// =============================================================================
// Types
// =============================================================================

/**
 * JSON-RPC request structure
 */
interface JsonRpcRequest {
  jsonrpc: '2.0'
  method: string
  params: unknown[]
  id: number
}

/**
 * JSON-RPC response structure
 */
interface JsonRpcResponse<T> {
  jsonrpc: '2.0'
  result?: T
  error?: {
    code: number
    message: string
    data?: unknown
  }
  id: number
}

/**
 * EVM log entry
 */
export interface EvmLog {
  /** Address from which this log originated */
  address: string
  /** Array of 0 to 4 32 Bytes DATA of indexed log arguments */
  topics: string[]
  /** Contains non-indexed arguments of the log */
  data: string
  /** Block number where this log was in (hex) */
  blockNumber: string
  /** Hash of the transaction this log was created from */
  transactionHash: string
  /** Index position of the transaction in the block (hex) */
  transactionIndex: string
  /** Hash of the block where this log was in */
  blockHash: string
  /** Index position in the block (hex) */
  logIndex: string
  /** true when the log was removed due to a chain reorg */
  removed: boolean
}

/**
 * Transaction receipt
 */
export interface TransactionReceipt {
  /** Hash of the transaction */
  transactionHash: string
  /** Index position of the transaction in the block (hex) */
  transactionIndex: string
  /** Hash of the block this transaction was in */
  blockHash: string
  /** Block number this transaction was in (hex) */
  blockNumber: string
  /** Address of the sender */
  from: string
  /** Address of the receiver (null for contract creation) */
  to: string | null
  /** Total gas used when this transaction was executed (hex) */
  cumulativeGasUsed: string
  /** Gas used by this specific transaction (hex) */
  gasUsed: string
  /** Contract address created if this was a contract creation, otherwise null */
  contractAddress: string | null
  /** Array of log objects generated by this transaction */
  logs: EvmLog[]
  /** Bloom filter for light clients to quickly retrieve related logs */
  logsBloom: string
  /** 1 (success) or 0 (failure) (hex) */
  status: string
  /** Post-transaction state root (pre-Byzantium) */
  root?: string
  /** Effective gas price (hex) */
  effectiveGasPrice?: string
  /** Transaction type (hex) */
  type?: string
}

/**
 * Block information
 */
export interface EvmBlock {
  /** Block number (hex) or null for pending */
  number: string | null
  /** Block hash or null for pending */
  hash: string | null
  /** Parent block hash */
  parentHash: string
  /** SHA3 of the uncles data */
  sha3Uncles: string
  /** Bloom filter for logs */
  logsBloom: string
  /** Root of the transaction trie */
  transactionsRoot: string
  /** Root of the state trie */
  stateRoot: string
  /** Root of the receipts trie */
  receiptsRoot: string
  /** Address of the miner */
  miner: string
  /** Mining difficulty (hex) */
  difficulty: string
  /** Total difficulty (hex) */
  totalDifficulty: string
  /** Extra data */
  extraData: string
  /** Block size in bytes (hex) */
  size: string
  /** Gas limit (hex) */
  gasLimit: string
  /** Gas used (hex) */
  gasUsed: string
  /** Block timestamp (hex) */
  timestamp: string
  /** Array of transaction hashes or transaction objects */
  transactions: string[] | object[]
  /** Array of uncle hashes */
  uncles: string[]
  /** Base fee per gas (hex, EIP-1559) */
  baseFeePerGas?: string
}

/**
 * Filter parameters for getLogs
 */
export interface GetLogsFilter {
  /** Block number (hex), "earliest", "latest", or "pending" */
  fromBlock?: string | number
  /** Block number (hex), "earliest", "latest", or "pending" */
  toBlock?: string | number
  /** Contract address or list of addresses */
  address?: string | string[]
  /** Array of topic filters */
  topics?: (string | string[] | null)[]
  /** Block hash (alternative to fromBlock/toBlock) */
  blockHash?: string
}

/**
 * Ethereum transaction object
 */
export interface EvmTransaction {
  hash: string
  nonce: string
  blockHash: string | null
  blockNumber: string | null
  transactionIndex: string | null
  from: string
  to: string | null
  value: string
  gas: string
  gasPrice: string
  input: string
  v: string
  r: string
  s: string
  type?: string
  maxFeePerGas?: string
  maxPriorityFeePerGas?: string
  chainId?: string
  accessList?: Array<{ address: string; storageKeys: string[] }>
  yParity?: string
}

/**
 * Options for creating an EVM RPC client
 */
export interface EvmRpcClientOptions {
  /** Request timeout in milliseconds (default: 30000) */
  timeout?: number
  /** Request timeout in milliseconds (preferred over timeout) */
  timeoutMs?: number
  /** Custom headers to include in requests */
  headers?: Record<string, string>
  /** Authentication configuration */
  auth?: AuthConfig
}

// =============================================================================
// Helper Functions
// =============================================================================

/**
 * Convert a number or bigint to 0x-prefixed hex string for JSON-RPC params.
 */
function numberToHex(value: number | bigint): string {
  return '0x' + value.toString(16)
}

/**
 * Convert a block number parameter to hex or block tag
 */
function formatBlockParam(block: string | number | undefined): string {
  if (block === undefined) return 'latest'
  if (typeof block === 'string') {
    if (block === 'latest' || block === 'earliest' || block === 'pending') {
      return block
    }
    // Already hex
    if (block.startsWith('0x')) return block
    // Number string
    return numberToHex(BigInt(block))
  }
  return numberToHex(block)
}

