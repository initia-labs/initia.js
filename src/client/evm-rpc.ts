/**
 * Create an EVM JSON-RPC client.
 *
 * @param endpoint - JSON-RPC endpoint URL
 * @param options - Client options
 * @returns EVM RPC client instance
 *
 * @example
 * ```typescript
 * import { createEvmRpcClient } from 'initia.js'
 *
 * const rpc = createEvmRpcClient('https://jsonrpc-evm-1.anvil.initia.xyz')
 *
 * // Get transaction receipt with decoded logs
 * const receipt = await rpc.getTransactionReceipt(txHash)
 * ```
 */
export function createEvmRpcClient(endpoint: string, options?: EvmRpcClientOptions): EvmRpcClient {
  return new EvmRpcClient(endpoint, options)
}
